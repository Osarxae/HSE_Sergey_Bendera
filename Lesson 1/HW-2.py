"""
Реализуйте структуру данных «Участники спора». Она должна представлять собой список (list) словарей (dict).
Каждый словарь хранит информацию об отдельном участнике. Словарь содержит пары «ключ-значение» параметрами, то есть характеристиками участника: наименование, статус, ИНН.
Заполнение данных должно быть произведено пользователем через консоль для трёх различных участников.
Пример готовой структуры:
[
{“name”: 'ООО "Рога и Копыта"', “status”: "Истец", “inn”: "4545454545"}, {“name”: 'Баширов А.А.', “status”: "Ответчик", “inn”: "32323232323232"}, {“name”: 'Петров А.А.', “status”: "Третье лицо", “inn”: "12121212121212"} ]
"""
# Создание пустого списка для хранения участников спора
participants = []

# Запрос у пользователя информации о трёх различных участниках
for i in range(3):
    # Запрос у пользователя информации о участнике
    name_str = input(f"Введите наименование участника {i + 1}: ")
    status_str = input(f"Введите статус участника {i + 1} (Истец, Ответчик, Третье лицо): ")
    inn_str = input(f"Введите ИНН участника {i + 1}: ")

    # Создание словаря с информацией о участнике
    participant = {
        "name": name_str,
        "status": status_str,
        "inn": inn_str
    }

    # Добавление словаря в список участников
    participants.append(participant)

# Вывод полученных данных в консоль
print("Участники спора:")
for participant in participants:
    print(participant)